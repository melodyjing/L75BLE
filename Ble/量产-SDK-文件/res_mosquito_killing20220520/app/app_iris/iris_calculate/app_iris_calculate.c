/*
初始化和建立一个任务来处理中断发送过来的消息
*/
int app_iris_calculate_init(unsigned int frequency)
{
			unsigned char i;
	
	    p_csem_tx_callback = NULL;
	    p_csem_rx_callback = NULL;
	
			app_csem_idle_mode();

			cobra_ceva_baseband_csem_register_write(0x017,0x00);
	
      if((frequency >= 2360) && (frequency <= 2401))   //带外 低 
		  {
			    for(i = 0; i < CSEM_RF_INIT_TBL_1MBPS_SIZE; i++)
			    {
					   cobra_ceva_baseband_csem_register_write(i, CSEM_RF_TBL_1MBPS[i]);
			    }		
	    }
		  else if((frequency >= 2481) && (frequency <= 2511))    //带外  高 
		  {
			  	for(i = 0; i < CSEM_RF_INIT_TBL_1MBPS_SIZE; i++)
			    {
					   cobra_ceva_baseband_csem_register_write(i, CSEM_RF_TBL_1MBPS_HIGH[i]);
			    }		
		  }
			else if((frequency >= 2402) && (frequency <= 2480))   //带内 
			{
				  for(i = 0; i < CSEM_RF_INIT_TBL_1MBPS_SIZE; i++)
			    {
					   cobra_ceva_baseband_csem_register_write(i, CSEM_RF_TBL_1MBPS_BLUE[i]);
			    }		
				
			}
		 
			return 0;	
}

/*
使能各种发送接受中断

*/
void app_csem_irq_enable(CSEM_IRQ_TYPE irq)
{
			unsigned char value;
			unsigned char temp;
			switch(irq)
			{
				case CSEM_IRQ_TX_FLAG:
					value = 0x01;
					break;
				case CSEM_IRQ_RXSTOP_FLAG:
					value = 0x02;
					break;
				case CSEM_IRQ_RECEIVED_FLAG:
					value = 0x04;
					break;
				case CSEM_IRQ_SYNC_FLAG:
					value = 0x08;
					break;
				case CSEM_IRQ_TXFIFO_FLAG:
					value = 0x10;
					break;
				case CSEM_IRQ_RXFIFO_FLAG:
					value = 0x20;
					break;
				default:
					value = 0x00;
					break;
				
			}
			temp = cobra_ceva_baseband_csem_register_read(CS565_REG_IRQ_CONF);
			temp |= value&0x3f;
		  cobra_ceva_baseband_csem_register_write(CS565_REG_IRQ_CONF, temp);
}

